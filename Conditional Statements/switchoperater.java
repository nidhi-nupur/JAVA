public class switchoperater {
    public static void main(String args[]) {
        int number = 1;
        switch(number) {
            case 3 : System.out.println("Java");
            break;
            case 1 : System.out.println("Python");
            break;
            case 2 : System.out.println("C++");
            break;
            default : System.out.println("NOTA");
        }
    }
}
